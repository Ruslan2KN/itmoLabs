package commands;

/**
 * Перечисление всех доступных серверных команд приложения.
 * Служит для идентификации команд, приходящих в сетевых запросах от клиента,
 * и их сопоставления с конкретными объектами-обработчиками в CommandManager.
 */
public enum Commands {
    /**
     * Добавление нового элемента в коллекцию.
     */
    add,

    /**
     * Полная очистка коллекции учебных групп.
     */
    clear,

    /**
     * Подсчет количества групп, у которых поле studentsCount равно заданному.
     */
    count_by_students_count,

    /**
     * Вывод справки по всем доступным серверным командам.
     */
    help,

    /**
     * Вывод информации о коллекции (тип, дата инициализации, количество элементов).
     */
    info,

    /**
     * Вывод всех элементов коллекции в порядке убывания.
     */
    print_descending,

    /**
     * Вывод имен администраторов всех групп в порядке убывания.
     */
    print_field_descending_group_admin,

    /**
     * Удаление конкретного элемента из коллекции по его идентификатору.
     */
    remove_by_id,

    /**
     * Удаление самого первого элемента в очереди.
     */
    remove_first,

    /**
     * Удаление всех элементов, которые больше заданного (на основе сравнения).
     */
    remove_greater,

    /**
     * Удаление всех элементов, которые меньше заданного (на основе сравнения).
     */
    remove_lower,

    /**
     * Вывод всех элементов коллекции в строковом представлении.
     */
    show,

    /**
     * Обновление данных существующего элемента коллекции по его ID.
     */
    update_id,

    /**
     * Внутренняя служебная команда для проверки существования ID на сервере.
     */
    check_id
}