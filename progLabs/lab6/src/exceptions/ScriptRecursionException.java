package src.exceptions;

/**
 * Исключение, выбрасываемое при обнаружении рекурсивного вызова скриптов.
 * Данный класс используется механизмом выполнения скриптов для предотвращения
 * бесконечного цикла (когда скрипт вызывает сам себя напрямую или через цепочку
 * других файлов), что защищает приложение от переполнения стека и зависания.
 */
public class ScriptRecursionException extends RuntimeException {

    /**
     * Конструктор, создающий новое исключение с сообщением о рекурсии.
     *
     * @param message Строковое сообщение, обычно содержащее путь к файлу,
     * который вызвал циклическую зависимость.
     */
    public ScriptRecursionException(String message) {
        super(message);
    }
}