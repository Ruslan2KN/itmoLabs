package src.exceptions;

/**
 * Исключение, выбрасываемое при возникновении ошибок в процессе сохранения коллекции в файл.
 * Данный класс используется для сигнализации о проблемах доступа к файловой системе,
 * неверно указанных путях в переменных окружения или ошибках записи данных в XML-формат.
 * Наследование от RuntimeException позволяет выбрасывать исключение без обязательного
 * объявления в сигнатуре методов.
 */
public class FileSaveException extends RuntimeException {

    /**
     * Конструктор, создающий новое исключение с подробным описанием причины ошибки.
     *
     * @param message Строковое сообщение, содержащее детали ошибки, которое
     * будет выведено пользователю или записано в лог сервера.
     */
    public FileSaveException(String message) {
        super(message);
    }
}