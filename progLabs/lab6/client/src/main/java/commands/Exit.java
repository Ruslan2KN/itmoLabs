package commands;

/**
 * Команда для немедленного завершения работы клиентского приложения.
 * Данная команда выполняет выход из программы без отправки запроса на сохранение
 * текущего состояния коллекции на сервер.
 */
public class Exit extends ClientCommand {

    /**
     * Конструктор, инициализирующий команду выхода.
     * Устанавливает уникальный идентификатор команды из перечисления ClientCommands
     * и текстовое описание для справки.
     */
    public Exit() {
        super(ClientCommands.exit, "завершить программу без сохранения в файл");
    }

    /**
     * Выполняет процедуру завершения работы приложения.
     * Метод проверяет отсутствие аргументов, так как команда exit выполняется
     * без параметров. Выводит в консоль прощальное сообщение и завершает
     * работу виртуальной машины Java.
     *
     * @param args Массив аргументов команды. Ожидается пустой массив.
     * @throws IllegalArgumentException Если пользователь передал дополнительные
     *                                  параметры при вызове команды.
     */
    @Override
    public void execute(String[] args) {
        if (args.length > 0) {
            throw new IllegalArgumentException("Ошибка, exit не принимает параметров");
        }

        System.out.println("Завершение работы клиенского приложения");
        System.exit(0);
    }
}