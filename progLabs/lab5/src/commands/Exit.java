package lab5.src.commands;

/**
 * Команда для немедленного завершения работы программы.
 * Выполняет выход из приложения без сохранения текущего состояния коллекции в файл.
 */
public class Exit extends Command {

    /**
     * Конструктор, инициализирующий команду выхода.
     * Устанавливает имя команды "exit" и её описание для справки.
     */
    public Exit() {
        super("exit", "завершить программу без сохранения в файл");
    }

    /**
     * Выполняет логику завершения программы.
     * Проверяет, что команде не переданы дополнительные аргументы, выводит прощальное сообщение
     * и штатно завершает работу виртуальной машины Java (с кодом 0).
     *
     * @param args массив строковых аргументов, переданных с командой (ожидается пустой массив)
     * @throws IllegalArgumentException если команде переданы какие-либо дополнительные параметры
     */
    @Override
    public void execute(String[] args) {
        if (args.length > 0) {
            throw new IllegalArgumentException("Ошибка, exit не принимает параметров");
        }

        System.out.println("Завершение работы");
        System.exit(0);
    }
}
