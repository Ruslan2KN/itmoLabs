package lab5.src.models;

/**
 * Интерфейс, определяющий контракт для проверки корректности (валидации) данных объекта.
 */
public interface Validator {

    /**
     * Проверяет состояние полей объекта на соответствие заданным правилам и ограничениям.
     * Вызывается перед добавлением объекта в коллекцию или после его изменения.
     *
     * @throws IllegalArgumentException если хотя бы одно из полей содержит недопустимое значение
     */
    void validate() throws IllegalArgumentException;
}
